public class NeitherPositiveNorNegative {

	public static void main(String[] args) {
		int num= -4; 
		
		if (num<0) {
			System.out.println("The number is Negative");
		}else if(num>0) {
			System.out.println("The number is Positive");
		}else if(num==0) {
			System.out.println("Neither the number is Positive Nor Negative");
		}

	}

}
